                                "Total Spent",
                                style: TextStyle(
                                  color: isDark
                                      ? theme.colorScheme.primary
                                      : Colors.white.withAlpha(200),
                                  fontSize: 14,
                                  fontWeight: FontWeight.w500,
                                ),
