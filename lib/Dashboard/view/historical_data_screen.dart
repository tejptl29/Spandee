              const SizedBox(height: 20),

              /// Dynamic Content
              Expanded(
                child: StreamBuilder<QuerySnapshot>(
                  stream: FirebaseFirestore.instance
                      .collection('users')
                      .doc(user!.uid)
                      .collection('expenses')
                      .where('date', isGreaterThanOrEqualTo: startOfMonth)
                      .where('date', isLessThan: nextMonth)
