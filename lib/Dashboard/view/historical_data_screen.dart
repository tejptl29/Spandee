class _HistoricalDataScreenState extends State<HistoricalDataScreen> {
  final user = FirebaseAuth.instance.currentUser;
  DateTime _selectedMonth = DateTime.now(); // Default to current month
  List<QueryDocumentSnapshot>?
  _currentMonthDocs; // To store data for PDF generation
  bool _showChart = false; // Toggle for chart visibility

  Future<void> _generatePdf(List<QueryDocumentSnapshot> docs) async {
    try {
      CustomToast.show(
        context,
        'Generating PDF Report...',
        type: ToastType.info,
      );

      final pdf = pw.Document();
      final font = await PdfGoogleFonts.interRegular();
      final fontBold = await PdfGoogleFonts.interBold();

      // Load Logo
      final logoBytes = await rootBundle.load(
        'assets/icon/Gemini_Generated_logo.png',
      );
      final logoImage = pw.MemoryImage(logoBytes.buffer.asUint8List());

      // Calculate total
      int totalAmount = 0;
      for (var doc in docs) {
        totalAmount += ((doc.data() as Map<String, dynamic>)['amount'] as num)
            .toInt();
      }

      final monthName = DateFormat('MMMM yyyy').format(_selectedMonth);

      pdf.addPage(
        pw.MultiPage(
          pageTheme: pw.PageTheme(
            pageFormat: PdfPageFormat.a4,
            buildBackground: (pw.Context context) {
              return pw.FullPage(
                ignoreMargins: true,
                child: pw.Center(
                  child: pw.Opacity(
                    opacity: 0.1,
                    child: pw.Image(logoImage, width: 300),
                  ),
                ),
              );
            },
          ),
          header: (pw.Context context) {
            return pw.Column(
              children: [
                pw.Row(
                  mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
                  children: [
                    pw.Text(
                      'Expense Report - $monthName',
                      style: pw.TextStyle(font: fontBold, fontSize: 24),
                    ),
                    pw.Image(logoImage, width: 50, height: 50),
                  ],
                ),
                pw.SizedBox(height: 20),
              ],
            );
          },
          footer: (pw.Context context) {
            return pw.Column(
              children: [
                pw.Divider(),
                pw.Row(
                  mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
                  children: [
                    pw.Text(
                      'Generated by Spendee',
                      style: pw.TextStyle(
                        font: font,
                        fontSize: 10,
                        color: PdfColors.grey500,
                      ),
                    ),
                    pw.Text(
                      DateFormat('dd MMM yyyy HH:mm').format(DateTime.now()),
                      style: pw.TextStyle(
                        font: font,
                        fontSize: 10,
                        color: PdfColors.grey500,
                      ),
                    ),
                  ],
                ),
              ],
            );
          },
          build: (pw.Context context) {
            return [
              // Summary Card
              pw.Container(
                padding: const pw.EdgeInsets.all(16),
                decoration: pw.BoxDecoration(
                  color: PdfColors.grey100,
                  borderRadius: pw.BorderRadius.circular(8),
                ),
                child: pw.Row(
                  mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
                  children: [
                    pw.Column(
                      crossAxisAlignment: pw.CrossAxisAlignment.start,
                      children: [
                        pw.Text(
                          'Total Spent',
                          style: pw.TextStyle(
                            font: font,
                            fontSize: 12,
                            color: PdfColors.grey600,
                          ),
                        ),
                        pw.Text(
                          'INR ${NumberFormat('#,##,###').format(totalAmount)}',
                          style: pw.TextStyle(
                            font: fontBold,
                            fontSize: 20,
                            color: PdfColors.green700,
                          ),
                        ),
                      ],
                    ),
                    pw.Text(
                      '${docs.length} Transactions',
                      style: pw.TextStyle(
                        font: font,
                        fontSize: 12,
                        color: PdfColors.grey600,
                      ),
                    ),
                  ],
                ),
              ),
              pw.SizedBox(height: 30),

              // Table
              pw.TableHelper.fromTextArray(
                border: pw.TableBorder.all(color: PdfColors.grey300),
                headerDecoration: const pw.BoxDecoration(
                  color: PdfColors.grey100,
                ),
                headerStyle: pw.TextStyle(font: fontBold),
                cellStyle: pw.TextStyle(font: font),
                headerAlignment: pw.Alignment.centerLeft,
                cellAlignment: pw.Alignment.centerLeft,
                columnWidths: {
                  0: const pw.FixedColumnWidth(100),
                  1: const pw.FlexColumnWidth(),
                  2: const pw.FixedColumnWidth(100),
                },
                headers: ['Date', 'Category', 'Amount'],
                data: List<List<dynamic>>.from(
                  docs.reversed.map((doc) {
                    final data = doc.data() as Map<String, dynamic>;
                    final date = (data['date'] as Timestamp).toDate();
                    final category = data['category'] ?? 'Other';
                    final amount = (data['amount'] as num).toInt();
                    return [
                      DateFormat('dd MMM yyyy').format(date),
                      category,
                      'INR ${NumberFormat('#,##,###').format(amount)}',
                    ];
                  }),
                ),
              ),
            ];
          },
        ),
      );

      // Share/Print the PDF
      await Printing.layoutPdf(
        onLayout: (PdfPageFormat format) async => pdf.save(),
        name: 'Spendee_Report_${monthName.replaceAll(' ', '_')}',
      );
    } catch (e) {
      debugPrint("Error generating PDF: $e");
      if (context.mounted) {
        CustomToast.show(
          context,
          'Error generating PDF: $e',
          type: ToastType.error,
        );
      }
    }
  }

  Future<void> _selectMonth(BuildContext context) async {
    final DateTime? picked = await showDatePicker(
      context: context,
      initialDate: _selectedMonth,
      firstDate: DateTime(2000),
      lastDate: DateTime.now(),
      initialDatePickerMode:
          DatePickerMode.year, // Encourage year/month selection
      // Note: standard Flutter date picker selects a day.
      // We will just use the month/year from the selected day.
      builder: (context, child) {
        final isDark = Provider.of<ThemeProvider>(
          context,
          listen: false,
        ).isDarkMode;
        return Theme(
          data: Theme.of(context).copyWith(
            colorScheme: isDark
                ? const ColorScheme.dark(
                    primary: Color(0xFF16B888), // Match app theme
                    onPrimary: Colors.white,
                    onSurface: Colors.white,
                  )
                : const ColorScheme.light(
                    primary: Color(0xFF16B888),
                    onPrimary: Colors.white,
                    onSurface: Colors.black87,
                  ),
          ),
          child: child!,
        );
      },
    );
    if (picked != null) {
      setState(() {
        _selectedMonth = picked;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    final themeProvider = Provider.of<ThemeProvider>(context);
    final isDark = themeProvider.isDarkMode;
    final theme = Theme.of(context);

    // Calculate start and end of selected month
    final startOfMonth = DateTime(_selectedMonth.year, _selectedMonth.month, 1);
