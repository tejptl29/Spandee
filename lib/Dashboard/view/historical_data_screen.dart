                                    child: Padding(
                                      padding: const EdgeInsets.symmetric(
                                        horizontal: 10,
                                      ),
                                      child: Row(
                                        mainAxisSize: MainAxisSize.min,
                                        children: [
                                          Icon(
                                            _showChart
                                                ? Icons.bar_chart
                                                : Icons.show_chart,
                                            color: const Color(0xFF16B888),
                                            size: 18,
                                          ),
                                          const SizedBox(width: 4),
                                          const Text(
                                            "Chart",
                                            style: TextStyle(
                                              color: Color(0xFF16B888),
                                              fontWeight: FontWeight.bold,
                                              fontSize: 13,
